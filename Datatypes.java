/* To declare variable
datatype variable_name;
datatype variable_name= value;
Identifier are attributes that are defined by the user
*/
class Datatypes{
	
	public static void main(String args[]){
		// These are primitive data types.
		// Because of this java is not 100% object-oriented programming language.
		byte s= 22;
		System.out.println(s);
		byte a=30;
		System.out.println("Value of a is "+ a );
		byte b= 35;
		System.out.println("***** "+b+"*****");
		
		short c= 10000;
		System.out.println("c is "+c);
		
		int d= 22022002;
		System.out.println("d is "+d);
		
		// for float values add "f" behind the value
		float e= 22.543f;
		System.out.println("e is "+e);
		
		// for long values add "l" behind the value
		long p= 123456789012344l;
		System.out.println(p);
		
		double q= 23.455675;
		System.out.println(q);
		
		char r= '$';
		System.out.println(r);
		
		boolean t=true;
		System.out.println(t);
		
	}
	

}